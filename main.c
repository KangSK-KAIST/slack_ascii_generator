int main(int argc, char** argv) {
  int toScreen = 0;
  if (argc == 2) {
    toScreen = 1;
  } else if (argc == 4) {
  } else {
    printf("Usage: message  (or)  emoji1(white) emoji2(black) message\n");
    return -1;
  }

  int len = 0;
  if (toScreen)
    len = strlen(argv[1]);
  else
    len = strlen(argv[3]);

  for (int y = 0; y < 5; y++) {
    for (int i = 0; i < len; i++) {
      // Select character
      int ch;
      if (toScreen)
        ch = (argv[1])[i];
      else
        ch = (argv[3])[i];
      if (('a' <= ch) && (ch <= 'z')) {
        ch += ('A' - 'a');
      } else if (('A' <= ch) && (ch <= 'Z')) {
      } else {
        printf("Only alphabet possible !\n");
        return -1;
      }
      // Print space in first column
      if (i == 0) {
        if (toScreen)
          printf("-");
        else
          printf(argv[1]);
      }
      // Print message
      for (int x = 0; x < 5; x++) {
        int p = alpha[ch - 'A'][y][x];
        if (toScreen) {
          if (p)
            printf("#");
          else
            printf("-");
        } else {
          if (p)
            printf("%s", argv[2]);
          else
            printf("%s", argv[1]);
        }
      }
      // Print space
      if (toScreen)
        printf("-");
      else
        printf("%s", argv[1]);
    }
    printf("\n");
  }
  return 0;
}
